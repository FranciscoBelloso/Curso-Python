'''Operaciones con Variables'''
# Se declarand dos variables y se inicializan
edad = 18
altura = 1.75

# Operaciones basicas en python
suma = edad+altura
resta = edad-altura
multiplicacion = edad*altura
divicion = edad/altura
residuo =  edad%altura
divicionEntera = edad//altura
cuadrado = edad**altura

print("""
    Suma: {},
    Resta: {},
    Multiplicacion: {},
    Divicion: {},
    Residuo: {},
    Divicion Entera: {},
    Cuadrado: {}""".format(suma,resta,multiplicacion,divicion,residuo,divicionEntera,cuadrado))